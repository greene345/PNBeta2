<h2>What is Pixel Nations, exactly?</h2>
Pixel Nations is a nation simulation game. Each player creates his own country, and the object of the game is to have the strongest country. Countries have cities, which make up their population,
 as well as an army for fighting wars. There is research and technology to help develop your nation, along with resources you can buy, sell, and trade.
